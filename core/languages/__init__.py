# SRE Language Packages
#
# Each subdirectory is a language package containing:
#   - language.yaml        (language registration)
#   - lexicon/             (entity → surface form mappings)
#   - grammar/             (grammar projection rules)
#   - audio/               (pronunciation audio files)
#   - media/               (cultural images, etc.)
#
# The compiler discovers language packages by scanning this directory.
